
public class dongu3 {

	public static void main(String[] args) {
		/*
		 *
		 * Java’da do-while döngüsü, şart en sonda kontrol edildiği için en az bir kez çalışan bir döngü yapısıdır.
		 
		 do {
		  // Çalıştırılacak kodlar
         } while (koşul);
         
         */
		int i=1;
		do {
			System.out.print(i+" ");
			i++;
		}while(i<=10);
	}

}
