public class operator3 {

	public static void main(String[] args) {
		
		 // == (eşitse) 1
		// != (eşit değilse) 1
		// < > >= <= (doğruysa) 1
		
		int sayi1=98;
		int sayi2=456;
		boolean sonuc=sayi2>sayi1;
		boolean sonuc2=sayi1==sayi2;
		//sayi1 sayi2 ye eşit midir? hayır->0(false)
		boolean sonuc3=sayi1!=sayi2;
		//sayi1 sayi2 ye  eşit değil midir? evet->1(true)
		
		
		System.out.println(sonuc);
		System.out.println(sonuc2);
		System.out.println(sonuc3);
		
	}

}
