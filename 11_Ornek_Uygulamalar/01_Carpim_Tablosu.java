
public class ornek {

	public static void main(String[] args) {
		// İç içe for döngüsü kullanarak çarpım tablosu çıktı verilecektir.
        for(int i=1;i<=10;i++) {
        	for(int j=1;j<=10;j++) {
        		System.out.println(i+"X"+j+"="+i*j);
        	}
        	System.out.println("---------------------");
        }
	}

}
